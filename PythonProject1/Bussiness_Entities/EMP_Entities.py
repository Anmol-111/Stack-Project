class Emp_Entities:
    __emp_id=0
    __name=""
    __age=0
    __salary=0.00
    __designation=""
    def get_emp_id(self):
        return self.__emp_id
    def set_emp_id(self, __emp_id):
        self.__emp_id=__emp_id
    def get_name(self):
        return self.__name
    def set_name(self, __name):
        self.__name=__name
    def get_age(self):
        return self.__age
    def set_age(self, __age):
        self.__age=__age
    def get_salary(self):
        return self.__salary
    def set_salary(self, __salary):
        self.__salary=__salary
    def get_designation(self):
        return self.__designation
    def set_designation(self, __designation):
        self.__designation=__designation